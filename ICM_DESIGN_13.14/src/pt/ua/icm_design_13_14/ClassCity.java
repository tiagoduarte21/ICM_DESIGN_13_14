package pt.ua.icm_design_13_14;

public class ClassCity {
	
	private String name;
	private int points;
	
	
	public ClassCity(String name, int points) {
		this.name = name;
		this.points = points;
	}
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPoints() {
		return points;
	}
	public void setPoints(int points) {
		this.points = points;
	}
	
	
	

}
